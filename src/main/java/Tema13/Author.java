package Tema13;

public class Author {
    String author;
    String allQuotes;
    String favourite;
    String QuotesForAuthor;

    public String getAuthor(){
        return author;
    }
    public String getAllQuotes(){
        return allQuotes;
    }
    public void setFavourite(){
        this.favourite = favourite;
    }
    public String getFavourite(){
        return favourite;
    }
    public String getQuotesForAuthor() {
        return QuotesForAuthor;
    }

}